async function hlsplayerplay(urlsrc){
  if (Hls.isSupported()) {
    var hls = new Hls();
    hls.loadSource(atob(urlsrc));
    hls.attachMedia(videoelement);
    hls.on(Hls.Events.MANIFEST_PARSED,function() {
      videoelement.play();
    });
  }
}

